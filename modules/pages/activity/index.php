    <script type="text/javascript">
        $(document).ready(function () {
             $('#btn-more').on('click', function () {
                $('body').addClass('no-scroll');
                $('.menu__additional').addClass('menu__additional--active');
                $('.btn-close-menu').addClass('btn-close-menu--active');
                $(".menu").append('<div class="overlay"></div>');

            });


            function close(eventClose){
                $(eventClose).on('click', function () {
                    $(".overlay").hide();
                    $('body').removeClass('no-scroll');
                    $('.menu__additional').removeClass('menu__additional--active');
                    $('.btn-close-menu').removeClass('btn-close-menu--active');
                });
            }
 
            close('#btn-close-menu');
            close('.overlay');

            $("select.select-activity").select2();
 
            $('#best-activity').slick({
                slidesToShow: 4,
                slidesToScroll: 1,
                arrows:true,
                prevArrow:"<button class='slick-prev pull-left'><i class='zmdi zmdi-arrow-left'></i></button>",
                nextArrow:"<button class='slick-next pull-right'><i class='zmdi zmdi-arrow-right'></i></button>",
                responsive: [
                {
                  breakpoint: 480,
                  settings: {
                    slidesToShow: 1,
                    slidesToScroll: 1,
                    arrows:false,
                    centerMode: true,
                    centerPadding: '60px'
                  }
                }]
            });

            var win = $(window).width();
            if( win < 480 ) {
                $('.list-item').slick({
                    slidesToShow: 1,
                    slidesToScroll: 1,
                    arrows:false,
                    centerMode: true,
                    centerPadding: '60px'
                });

                $(window).on('scroll', function () {
                    if($(this).scrollTop() > 50) {
                        $('ul.menu__main').addClass('is-hide');
                    } else {
                        $('ul.menu__main').removeClass('is-hide');
                    }
                });
            }
           
            

        })
    </script>

